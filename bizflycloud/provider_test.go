package bizflycloud

import (
	"os"
	"testing"

	"github.com/hashicorp/terraform-plugin-sdk/helper/schema"
	"github.com/hashicorp/terraform-plugin-sdk/terraform"
)

var (
	testAccProviders map[string]terraform.ResourceProvider
	testAccProvider  *schema.Provider
)

func init() {
	testAccProvider = Provider().(*schema.Provider)
	testAccProviders = map[string]terraform.ResourceProvider{
		"bizflycloud": testAccProvider,
	}
}

func TestProvider(t *testing.T) {
	if err := Provider().(*schema.Provider).InternalValidate(); err != nil {
		t.Fatalf("err: %s", err)
	}
}

func TestProvider_impl(t *testing.T) {
	var _ terraform.ResourceProvider = Provider()
}

func testAccPreCheck(t *testing.T) {
	if v := os.Getenv("BIZFLYCLOUD_EMAIL"); v == "" {
		t.Fatal("BIZFLYCLOUD_EMAIL must be set for acceptance tests")
	}
	if v := os.Getenv("BIZFLYCLOUD_PASSWORD"); v == "" {
		t.Fatal("BIZFLYCLOUD_PASSWORD must be set for acceptance tests")
	}
}
